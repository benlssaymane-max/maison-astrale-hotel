function setStatus(message, isError = false) {
  if (!bookingStatus) {
    return;
  }

  bookingStatus.textContent = message;
  bookingStatus.classList.toggle("error", isError);
}

function renderQuote(quote) {
  if (!summaryLine) {
    return;
  }

  const extras = upsellTotalCents();
  const total = quote.totalCents + extras;
  const extrasText = extras > 0 ? `, ${uiText[currentLang].extras} ${formatCurrency(extras)}` : "";
  summaryLine.textContent = currentLang === "fr"
    ? `${quote.roomName}, ${quote.nights} nuit(s), ${uiText[currentLang].total} ${formatCurrency(total)}${extrasText}.`
    : `${quote.roomName}, ${quote.nights} night(s), ${uiText[currentLang].total} ${formatCurrency(total)}${extrasText}.`;
}

async function refreshQuote() {
  const payload = getBookingPayload();

  updateBookingSteps();

  if (!payload.checkin || !payload.checkout) {
    latestQuote = null;
    if (summaryLine) {
      summaryLine.textContent = translations[currentLang].summary_idle;
    }
    setStatus("");
    return;
  }

  if (payload.checkout <= payload.checkin) {
    latestQuote = null;
    setStatus(uiText[currentLang].invalidDates, true);
    return;
  }

  setStatus(uiText[currentLang].checking);

  try {
    const response = await fetch("/api/check-availability", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(payload)
    });

    const data = await response.json();
    if (!response.ok) {
      latestQuote = null;
      setStatus(response.status === 409 ? uiText[currentLang].unavailable : uiText[currentLang].genericError, true);
      return;
    }

    latestQuote = data;
    renderQuote(data);
    setStatus(uiText[currentLang].available);
  } catch {
    latestQuote = null;
    setStatus(uiText[currentLang].genericError, true);
  }
}

let quoteDebounce;
[roomSelect, guestsSelect, checkinInput, checkoutInput, bookingForm?.elements?.upsell_spa, bookingForm?.elements?.upsell_transfer, bookingForm?.elements?.upsell_dinner, bookingForm?.elements?.name, bookingForm?.elements?.email].forEach((input) => {
  input?.addEventListener("change", () => {
    clearTimeout(quoteDebounce);
    quoteDebounce = setTimeout(refreshQuote, 200);
  });
  input?.addEventListener("input", updateBookingSteps);
});

bookingForm?.addEventListener("submit", async (event) => {
  event.preventDefault();

  const payload = getBookingPayload();
  if (!payload.name || !payload.email || !payload.checkin || !payload.checkout) {
    setStatus(uiText[currentLang].genericError, true);
    return;
  }

  if (payload.checkout <= payload.checkin) {
    setStatus(uiText[currentLang].invalidDates, true);
    return;
  }

  checkoutBtn.disabled = true;
  setStatus(uiText[currentLang].processing);

  try {
    const response = await fetch("/api/create-checkout-session", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(payload)
    });

    const data = await response.json();
    if (!response.ok || !data.checkoutUrl) {
      throw new Error(data.error || "checkout_failed");
    }

    window.location.href = data.checkoutUrl;
  } catch {
    setStatus(uiText[currentLang].genericError, true);
    checkoutBtn.disabled = false;
  }
});

