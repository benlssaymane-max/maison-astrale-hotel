let currentLang = "fr";

const translatePage = (lang) => {
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const value = translations[lang][key];
    if (value) {
      el.textContent = value;
    }
  });

  const audioLabel = document.getElementById("audioToggle");
  if (audioLabel && !audioLabel.dataset.playing) {
    audioLabel.textContent = lang === "fr" ? "Ambiance: Off" : "Ambience: Off";
  }
};

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18 }
);

document.querySelectorAll(".reveal").forEach((item) => revealObserver.observe(item));

const langToggle = document.getElementById("langToggle");
langToggle?.addEventListener("click", () => {
  currentLang = currentLang === "fr" ? "en" : "fr";
  langToggle.textContent = currentLang === "fr" ? "EN" : "FR";
  translatePage(currentLang);
});

const audio = document.getElementById("ambienceAudio");
const audioToggle = document.getElementById("audioToggle");
audioToggle?.addEventListener("click", async () => {
  if (!audio) {
    return;
  }

  try {
    if (audio.paused) {
      await audio.play();
      audioToggle.dataset.playing = "true";
      audioToggle.textContent = currentLang === "fr" ? "Ambiance: On" : "Ambience: On";
    } else {
      audio.pause();
      audioToggle.dataset.playing = "";
      audioToggle.textContent = currentLang === "fr" ? "Ambiance: Off" : "Ambience: Off";
    }
  } catch {
    audioToggle.textContent = currentLang === "fr" ? "Audio bloqué par le navigateur" : "Audio blocked by browser";
  }
});

const conciergeToggle = document.getElementById("conciergeToggle");
const conciergePanel = document.getElementById("conciergePanel");
const chatResponse = document.getElementById("chatResponse");

conciergeToggle?.addEventListener("click", () => {
  if (!conciergePanel) {
    return;
  }

  conciergePanel.hidden = !conciergePanel.hidden;
});

